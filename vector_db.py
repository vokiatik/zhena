model = load_model(model_name)

client = QdrantClient(":memory:")

client.create_collection(
    collection_name="entities",
    vectors_config=VectorParams(size=768, distance=Distance.COSINE),
)

client.create_collection(
    collection_name="metrics",
    vectors_config=VectorParams(size=768, distance=Distance.COSINE),
)

