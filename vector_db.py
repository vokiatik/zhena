print("Upserting brands into Qdrant...")
for i, brand in enumerate(brand_example_db):
    vector = model.encode(
        [brand["brand"]],
        normalize_embeddings=True
    )[0]
    print(f"Text: {brand['brand']}, Vector shape: {vector.shape}")
    client.upsert(
        collection_name="entities",
        points=[
            PointStruct(
                id=i,
                vector=vector.tolist(),
                payload={
                    "text": brand["brand"],
                    "description": brand["description"],
                    "sql_snippet": brand["sql_snippet"]
                }
            )
        ]
    )
    print(f"Upserted brand: {brand['brand']} with ID: {brand['id']}")

